    private void ButtonCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastroActionPerformed
        int x;
        String y;
        x = Integer.parseInt((String) jComboqtd.getSelectedItem());
        y = ((String)TextPreco.getText());
        if ( !"".equals(y) && !TextCadastro.getText().equals("")  ) {
            Estoque est = new Estoque();
            est.setNome(TextCadastro.getText());
            est.setPreco(Double.parseDouble(TextPreco.getText()));
            est.setQuantidade(x);
            crudEstoque crud = new crudEstoque();
            crud.create(est);
            JOptionPane.showMessageDialog(this, "Nome do produto: " + TextCadastro.getText() + " Preco: " + Double.parseDouble(TextPreco.getText()) + " Quantidade: " + x,
                    "Cadastro realizado com sucesso", JOptionPane.INFORMATION_MESSAGE);
            TextCadastro.setText("");
            TextPreco.setText("");

        } else {
            JOptionPane.showMessageDialog(rootPane, "Insira o texto");
        }
    }//GEN-LAST:event_ButtonCadastroActionPerformed

