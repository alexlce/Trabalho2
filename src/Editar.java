public void readjTableEdit() {
        DefaultTableModel modelTable = (DefaultTableModel) jTableEdit.getModel();
        modelTable.setNumRows(0);
        Estoque est = new Estoque();
        crudEstoque crud = new crudEstoque();
        for (Estoque e : crud.read(est)) {

            modelTable.addRow(new Object[]{
                e.getCodProduto(),
                e.getNome(),
                e.getPreco(),
                e.getQuantidade()
            });

        }

    }

