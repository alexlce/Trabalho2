    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int linha, coluna, dado;
        crudEstoque crud = new crudEstoque();
        Estoque est = new Estoque();

        linha = jTableEdit.getSelectedRow();
        dado = (int) jTableEdit.getValueAt(linha, 0);
        est.setCodProduto(dado);
        crud.delete(est);
        JOptionPane.showMessageDialog(rootPane, "Dados Excluidos com sucesso");
        readjTableEdit();

    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void ButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAlterarActionPerformed

        crudEstoque crud = new crudEstoque();
        Estoque est = new Estoque();

        int x;
        x = Integer.parseInt((String) jComboqtdEdit.getSelectedItem());
        if (jTableEdit.getSelectedRow() != -1) {

            est.setNome(TextProdEditar.getText());
            est.setPreco(Double.parseDouble((TextPrecoEdit.getText())));
            est.setQuantidade(x);
            est.setCodProduto((int) jTableEdit.getValueAt(jTableEdit.getSelectedRow(), 0));
            crud.update(est);
            JOptionPane.showMessageDialog(rootPane, "Dados Alterados com sucesso");
            readjTableEdit();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Você não selecionou dados para alterar");
        }
    }//GEN-LAST:event_ButtonAlterarActionPerformed


